# using range function you can loop over the range from 1 to 100 (exclusive)
for number in range(1, 10):
    print(number)

print('-'*50)

# using range reversed 
for number in range(10, 1, -1):
    print(number)

print('-'*50)

# range with intervals 
for number in range(1, 10, 3):
    print(number)

    