# function are code block in which you give some input and after executing logic inside that code block it returns some output 

def add(a, b):
    return a + b 

def multiply(a, b):
    return a*b 

def divide(a, b):
    return round(a/b, 2)

print(add(2, 3))
print(multiply(5, 6))
print(divide(6, 4))

