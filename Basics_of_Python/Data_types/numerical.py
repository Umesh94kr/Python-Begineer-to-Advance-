# 1. integer data type (which stores only integer values)
int_value = 5
print(f"Data type of int_value : {type(int_value)}")
# this show it belongs to class 'int' 

# 2. float -> represent decimals 
float_value = 0.06
print(f"Data type of float_value : {type(float_value)}")

# 3. String data type which stores sequence of characters 
str_value = "Umesh"
print(f"Data type of str_value : {type(str_value)}")

# 4. Boolean data type (True or False)
bool_value = True 
print(f"Data type of bool_value : {bool_value}")
